public class SuperMan {

    //躯体
    private String body;
    //特殊技能
    private String specialTalent;
    //身份标记
    private String specialSymol;

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getSpecialTalent() {
        return specialTalent;
    }

    public void setSpecialTalent(String specialTalent) {
        this.specialTalent = specialTalent;
    }

    public String getSpecialSymol() {
        return specialSymol;
    }

    public void setSpecialSymol(String specialSymol) {
        this.specialSymol = specialSymol;
    }
}
