package com.jiankang.otherstudy.v3.v1;

/**
 * Student
 *
 * @author jiankang.xu
 * @date 2020/3/14
 */
public class Student {
    private Long id;
    private String name;
    private Long age;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getAge() {
        return age;
    }

    public void setAge(Long age) {
        this.age = age;
    }
}
